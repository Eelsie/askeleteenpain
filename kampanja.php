          <h2>Ilmastonmuutos on ihmisoikeuskysymys</h2>
          <p style="margin-bottom: 22px;">Ilmastonmuutos on suurin ihmiskuntaa kohdannut haaste. Jos sitä ei saada kuriin, uhkaa maapallosta tulla elinkelvoton. Ilmastonmuutos seurauksineen on suurin lasten ja nuorten tulevaisuuteen kohdistuva uhkakuva. Hillitsemättömän ilmastonmuutoksen myötä maapallon keskilämpötila saattaa nousta yli 5C° vuosisadan aikana. Näin voimakkaalla lämpenemisellä on katastrofaalisia vaikutuksia. Merenpinta nousee, äärimmäiset sääilmiöt yleistyvät ja viljelyalueita tuhoutuu.</p>
          <a class="luelisaa" style="line-height: 100%" href="#vetoomus">Tästä pääset kirjoittamaan ilmastovetoomuksen suomen ministereille</a>

          <p style="margin-top: 17px;">Ilmastonmuutos on ennen kaikkea oikeudenmukaisuuskysymys. Sen seurauksista kärsivät eniten tulevaisuuden sukupolvet ja heikoimmassa asemassa olevat ihmisryhmät kehitysmaissa, jotka ovat vähiten vastuussa ongelman aiheuttamisesta. Suurin vastuu ilmastonmuutoksesta on vaurailla teollisuusmailla, kuten Suomella. Joulukuussa on tarkoitus sopia uudesta, kaikkia maailman maita koskevasta ilmastosopimuksesta Pariisissa: Minkälaista ilmastopolitiikkaa Suomi ajaa? Olemmeko edelläkävijöitä vai perässähiihtäjiä? Otammeko askeleen eteen vai taakse?</p>

          <p class="sitaatti">”Jopa 80 % ilmastonmuutoksen aiheuttamista kuolemista ja sairastumisista aiheutuu lapsille. Vaadimme, että päättäjät ryhtyvät huomioimaan ilmastonmuutoksen päätöksenteossaan, jotta mahdollisimman monella lapsella olisi hyvät edellytykset elämään. Lapset ovat maapallomme tulevaisuus. Haluamme turvata kaikille lapsille ympäri maailman mahdollisuuden turvalliseen ja terveelliseen tulevaisuuteen, ja se tehdään mahdottomaksi, jos maapallon tuhoaminen jatkuu.” (Lastenhallitus)</p>

          <p>Vaikka lapset ja nuoret kärsivät ilmastonmuutoksesta tulevaisuudessa enemmän, voivat he myös toimia suunnannäyttäjinä ja olla aktiivisia toimijoita ilmastonmuutoksen etenemisen pysäyttäjinä. Lapsen oikeuksien sopimuksessa määritellään lapsen oikeudeksi osallistua itseään koskevaan päätöksentekoon. Vaadimme, että lapsia ja nuoria kuullaan aikamme keskeisimmässä ympäristö- ja ihmisoikeuskysymyksessä. Otetaan #askeleteen kohti kunnianhimoista ilmastopolitiikkaa!</p>

          <p class="sitaatti">”Emme vaadi ihmeitä. Vaadimme, että Suomen hallitus ottaa myös meidän tulevaisuutemme huomioon ja toimii sille annetuin valtuuksin ilmastonmuutoksen pysäyttämiseksi. Nykyiset päättäjät eivät joudu kohtaamaan ilmastonmuutoksen kehittymistä yhtä pitkälle kuin me. Se ei oikeuta heitä pilaamaan meidän tulevaisuuttamme. Mitä myöhemmin tilanteeseen puututaan, sitä rajummin se täytyy tehdä.” (Mitä-verkosto)</p>

          <p>Kiihtyvä ilmastonmuutos ei odota parempia taloudellisia aikoja tai neuvotteluehtojen loputonta viilaamista. Kysymys on lasten ja nuorten tulevaisuudesta eli siitä, millaiset elämän edellytykset heille jätetään. Pariisissa on saatava aikaan vahva ja kansainvälisesti sitova sopimus, jonka toimenpiteet ovat riittäviä hillitsemään ilmastonmuutosta.</p>

          <p>Puheiden aika on ohi, nyt tarvitaan konkreettisia toimia. Planin lastenhallitus ja Mitä-verkosto yhdessä Kepan ja Ilmastovanhempien kanssa kutsuvat lapset ja nuoret sekä koko kansalaisyhteiskunnan ottamaan kantaa ilmastonmuutoksen torjumiseksi, jotta lasten oikeudet voivat toteutua myös tulevaisuudessa. Työ on aloitettava heti. Osallistu kampanjaan osallistumalla <a class="pienia" href="kuvahaaste">kuvahaasteeseen</a> tai kirjoittamalla oma viestisi päättäjille.</p>
          </br>
          <p><strong>Vaadimme, että Suomen hallitus kantaa vastuunsa ja varmistaa lasten ja nuorten mahdollisuudet hyvään elämään kaikkialla maailmassa:</strong></p>
          <ul class="vaadimme">
            <li class="list"><span>edistämällä oikeudenmukaista ja laillisesti sitovaa kansainvälistä ilmastosopimusta YK:n ilmastoneuvotteluissa Pariisissa joulukuussa</span></li>
            <li class="list"><span>aloittamalla heti työn hiilivoiman korvaamiseksi kestävällä uusiutuvalla energialla</span></li>
            <li class="list"><span>tukemalla kehitysyhteistyötä ja ilmastorahoitusta EU:n päästökaupasta saatavilla tuloilla</span></li>
          </ul>
